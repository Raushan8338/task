public class Product2 {
    private int id;
    private String date;
    private String message;

    public Product2(int id, String date, String message) {
        this.id = id;
        this.date = date;
        this.message = message;
    }

    public int getId() {
        return id;
    }

    public String getdate() {
        return date;
    }

    public String getmessage() {
        return message;
    }
}
